package Java_code_from_zero_to_master;

import java.util.HashSet;

public class HashSetExample {
    public static void main(String[] args) {
        HashSet<Integer> hs = new HashSet<Integer>();
        hs.add(85);
        hs.add(68);
        hs.add(45);
        hs.add(23);
        System.out.println(hs);
    }
}
